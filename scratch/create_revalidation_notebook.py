        "            \n"
        "            log_df.at[idx, 'actual_views'] = actual_views\n"
        "            log_df.at[idx, 'actual_virality_multiplier'] = round(actual_multiplier, 3)\n"
        "            log_df.at[idx, 'is_revalidated'] = 1\n"
