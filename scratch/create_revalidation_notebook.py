        "def revalidate_logged_predictions():\n"
        "    log_path = '../predictions/predictions_log.csv'\n"
        "    if not os.path.exists(log_path):\n"
        "        print('No predictions logged yet. Run 05_prediction_logging.ipynb first.')\n"
        "        return\n"
        "        \n"
        "    log_df = pd.read_csv(log_path)\n"
        "    \n"
        "    # Check if there are any rows with a video ID that are not yet revalidated\n"
        "    pending_mask = (log_df['published_video_id'].notna()) & \\\n"
        "                   (log_df['published_video_id'] != '') & \\\n"
        "                   (log_df['published_video_id'].str.strip() != '') & \\\n"
        "                   (log_df['is_revalidated'] == 0)\n"
        "    \n"
        "    pending = log_df[pending_mask]\n"
        "    \n"
        "    if len(pending) == 0:\n"
        "        print('No pending videos to revalidate.')\n"
        "        print('Instruction: Add the actual YouTube Video ID to \"published_video_id\" in predictions/predictions_log.csv to trigger revalidation.')\n"
        "        return\n"
        "        \n"
        "    # Load model extractor niche medians\n"
        "    try:\n"
        "        model_data = joblib.load('../src/model.joblib')\n"
